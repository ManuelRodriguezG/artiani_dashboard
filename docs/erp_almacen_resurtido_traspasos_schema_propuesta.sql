CREATE TABLE IF NOT EXISTS erp_inventario_politicas_almacen_sku (
  id_politica_almacen_sku INT NOT NULL AUTO_INCREMENT,
  id_almacen INT NOT NULL,
  id_sku_erp BIGINT NOT NULL,
  stock_minimo DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
  stock_maximo DECIMAL(18,6) NULL,
  punto_reorden DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
  cantidad_sugerida DECIMAL(18,6) NULL,
  dias_cobertura_objetivo INT NOT NULL DEFAULT 0,
  prioridad VARCHAR(30) NOT NULL DEFAULT 'normal',
  estatus VARCHAR(30) NOT NULL DEFAULT 'activa',
  observaciones TEXT NULL,
  creado_por INT NULL,
  actualizado_por INT NULL,
  fecha_registro DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  fecha_actualizacion DATETIME NULL,
  PRIMARY KEY (id_politica_almacen_sku),
  UNIQUE KEY uk_inv_politica_almacen_sku (id_almacen, id_sku_erp),
  KEY idx_inv_politica_sku (id_sku_erp),
  KEY idx_inv_politica_estatus (estatus),
  CONSTRAINT fk_inv_pol_almacen FOREIGN KEY (id_almacen) REFERENCES erp_almacenes (id_almacen),
  CONSTRAINT fk_inv_pol_sku FOREIGN KEY (id_sku_erp) REFERENCES erp_catalogo_skus (id_sku)
);

CREATE TABLE IF NOT EXISTS erp_almacen_resurtidos (
  id_resurtido_almacen INT NOT NULL AUTO_INCREMENT,
  folio VARCHAR(60) NOT NULL,
  tipo_documento VARCHAR(30) NOT NULL DEFAULT 'resurtido_tienda',
  id_almacen_solicitante INT NOT NULL,
  id_almacen_origen INT NULL,
  id_almacen_transito INT NULL,
  estatus VARCHAR(40) NOT NULL DEFAULT 'borrador',
  prioridad VARCHAR(30) NOT NULL DEFAULT 'normal',
  origen_solicitud VARCHAR(40) NOT NULL DEFAULT 'manual',
  fecha_solicitud DATETIME NULL,
  fecha_autorizacion DATETIME NULL,
  fecha_preparacion DATETIME NULL,
  fecha_envio DATETIME NULL,
  fecha_recepcion DATETIME NULL,
  fecha_cierre DATETIME NULL,
  solicitado_por INT NULL,
  autorizado_por INT NULL,
  preparado_por INT NULL,
  enviado_por INT NULL,
  recibido_por INT NULL,
  cerrado_por INT NULL,
  observaciones TEXT NULL,
  motivo_cancelacion TEXT NULL,
  fecha_registro DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  fecha_actualizacion DATETIME NULL,
  PRIMARY KEY (id_resurtido_almacen),
  UNIQUE KEY uk_alm_resurtido_folio (folio),
  KEY idx_alm_resurtido_solicitante (id_almacen_solicitante),
  KEY idx_alm_resurtido_origen (id_almacen_origen),
  KEY idx_alm_resurtido_transito (id_almacen_transito),
  KEY idx_alm_resurtido_estatus (estatus),
  KEY idx_alm_resurtido_fecha (fecha_registro),
  CONSTRAINT fk_res_alm_solicitante FOREIGN KEY (id_almacen_solicitante) REFERENCES erp_almacenes (id_almacen),
  CONSTRAINT fk_res_alm_origen FOREIGN KEY (id_almacen_origen) REFERENCES erp_almacenes (id_almacen),
  CONSTRAINT fk_res_alm_transito FOREIGN KEY (id_almacen_transito) REFERENCES erp_almacenes (id_almacen)
);

CREATE TABLE IF NOT EXISTS erp_almacen_resurtido_detalle (
  id_resurtido_detalle INT NOT NULL AUTO_INCREMENT,
  id_resurtido_almacen INT NOT NULL,
  id_sku_erp BIGINT NOT NULL,
  id_producto INT NULL,
  sku VARCHAR(120) NULL,
  nombre_producto VARCHAR(255) NULL,
  unidad_base VARCHAR(40) NULL,
  cantidad_solicitada DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
  cantidad_autorizada DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
