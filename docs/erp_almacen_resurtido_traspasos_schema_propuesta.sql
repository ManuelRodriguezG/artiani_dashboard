  id_resurtido_recepcion INT NULL,
  tipo_diferencia VARCHAR(40) NOT NULL,
  severidad VARCHAR(30) NOT NULL DEFAULT 'media',
  id_sku_erp BIGINT NULL,
  id_inventario_unidad INT NULL,
  cantidad_esperada DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
  cantidad_recibida DECIMAL(18,6) NOT NULL DEFAULT 0.000000,
