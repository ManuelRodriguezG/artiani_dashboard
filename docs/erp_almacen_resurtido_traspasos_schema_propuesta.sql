  CONSTRAINT fk_res_dif_envio FOREIGN KEY (id_resurtido_envio) REFERENCES erp_almacen_resurtido_envios (id_resurtido_envio),
  CONSTRAINT fk_res_dif_recepcion FOREIGN KEY (id_resurtido_recepcion) REFERENCES erp_almacen_resurtido_recepciones (id_resurtido_recepcion),
